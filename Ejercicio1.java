package ejerciciosVariables;

public class Ejercicio1 {
	
	public static void main(String[] args) {
		
		double num1 = 14;
		
		double num2 = 6;
		
		System.out.println("La suma de " + num1 + " y " + num2 + " es: " + (num1+num2));
		
		System.out.println("La resta de " + num1 + " menos " + num2 + " es: " + (num1-num2));
		
		System.out.println("Multiplicar " + num1 + " por " + num2 + " nos da como resultado: " + (num1*num2));
		
		System.out.println("Divide " + num1 + " entre " + num2 + " para obtener: " + num1/num2);
		
		System.out.println("El resto de dividir " + num1 + " entre " + num2 + " es: " + num1%num2);
		
	}

}
